#Variable declaration
number1 = 10
number2 = 5
#Declaration of result that stores the difference between the 2 numbers
result = number1 + number2
result1 = number1 - number2
result2 = number1 * number2
#Output on screen
print(f"Addition of {number1} and {number2} is {result}")
print(f"Subtraction of {number1} and {number2} is {result1}")
print(f"Multiplication of {number1} and {number2} is {result2}")